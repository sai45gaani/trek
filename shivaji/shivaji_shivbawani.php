document.addEventListener('DOMContentLoaded', function() {
    // Verse Card Animations
    function animateVerseCards() {
        const verseCards = document.querySelectorAll('.verse-card');
        const cardObserver = new IntersectionObserver((entries) => {
            entries.forEach((entry, index) => {
                if (entry.isIntersecting) {
                    setTimeout(() => {
                        entry.target.style.opacity = '1';
                        entry.target.style.transform = 'translateY(0) rotateX(0)';
                    }, index * 200);
                    cardObserver.unobserve(entry.target);
                }
            });
        }, { threshold: 0.1, rootMargin: '50px' });
        
        verseCards.forEach(card => {
            card.style.opacity = '0';
            card.style.transform = 'translateY(50px) rotateX(10deg)';
            card.style.transition = 'opacity 0.8s ease, transform 0.8s ease';
            cardObserver.observe(card);
        });
    }
    
    // Poetry Reading Effect
    function setupPoetryEffects() {
        const verseCards = document.querySelectorAll('.verse-card');
        verseCards.forEach(card => {
            card.addEventListener('mouseenter', function() {
                this.style.transform = 'scale(1.02) translateY(-5px)';
                this.style.boxShadow = '0 25px 50px rgba(245, 158, 11, 0.2)';
                
                // Add golden glow effect
                const verse = this.querySelector('.font-devanagari');
                if (verse) {
                    verse.style.textShadow = '0 0 10px rgba(245, 158, 11, 0.3)';
                }
            });
            
            card.addEventListener('mouseleave', function() {
                this.style.transform = 'scale(1) translateY(0)';
                this.style.boxShadow = '';
                
                const verse = this.querySelector('.font-devanagari');
                if (verse) {
                    verse.style.textShadow = '';
                }
            });
            
            // Click to highlight verse
            card.addEventListener('click', function() {
                this.style.animation = 'pulse 0.6s ease-in-out';
                setTimeout(() => {
                    this.style.animation = '';
                }, 600);
            });
        });
    }
    
    // Literary Analysis Cards
    function setupAnalysisCards() {
        const analysisCards = document.querySelectorAll('.bg-gradient-to-br');
        analysisCards.forEach(card => {
            card.addEventListener('mouseenter', function() {
                this.style.transform = 'scale(1.08) rotate(1deg)';
                this.style.boxShadow = '0 15px 30px rgba(0, 0, 0, 0.15)';
            });
            
            card.addEventListener('mouseleave', function() {
                this.style.transform = 'scale(1.05) rotate(0deg)';
                this.style.boxShadow = 'none';
            });
        });
    }
    
    // Text Animation for Hero
    function animateHeroText() {
        const heroTitle = document.querySelector('h1');
        if (heroTitle) {
            const text = heroTitle.textContent;
            heroTitle.textContent = '';
            
            let index = 0;
            function typeWriter() {
                if (index < text.length) {
                    heroTitle.textContent += text.charAt(index);
                    index++;
                    setTimeout(typeWriter, 100);
                }
            }
            
            setTimeout(typeWriter, 500);
        }
    }
    
    // Smooth Scrolling
    function setupSmoothScrolling() {
        document.querySelectorAll('a[href^="#"]').forEach(anchor => {
            anchor.addEventListener('click', function (e) {
                e.preventDefault();
                const target = document.querySelector(this.getAttribute('href'));
                if (target) {
                    const headerHeight = 80;
                    const targetPosition = target.offsetTop - headerHeight;
                    window.scrollTo({
                        top: targetPosition,
                        behavior: 'smooth'
                    });
                }
            });
        });
    }
    
    // Parallax Effect for Background
    function setupParallax() {
        window.addEventListener('scroll', function() {
            const scrolled = window.pageYOffset;
            const heroSection = document.querySelector('section');
            
            if (heroSection) {
                const speed = scrolled * 0.5;
                heroSection.style.transform = `translateY(${speed}px)`;
            }
        });
    }
    
    // Verse Highlighting on Scroll
    function setupVerseHighlighting() {
        const verses = document.querySelectorAll('.font-devanagari');
        const verseObserver = new IntersectionObserver((entries) => {
            entries.forEach(entry => {
                if (entry.isIntersecting) {
                    entry.target.classList.add('highlighted-verse');
                } else {
                    entry.target.classList.remove('highlighted-verse');
                }
            });
        }, { threshold: 0.7 });
        
        verses.forEach(verse => verseObserver.observe(verse));
    }
    
    // Initialize all functions
    animateVerseCards();
    setupPoetryEffects();
    setupAnalysisCards();
    animateHeroText();
    setupSmoothScrolling();
    setupParallax();
    setupVerseHighlighting();
    
    console.log('Shivbawani by Kavi Bhushan page loaded successfully');
});

