        Unfortuanately I could not surf net before going otherwise I could have made inquires with
        locals about these spots.
      </p>

      <p>
        Anyway, another important encounter was NAGA shadhu of Jain Gigamber faith. This was my
        second encounter, earlier being at the path of Amarnath caves. Later on return to mumbai,
        I tried to collect information from my Jain neighbors, as to how these Shadhu’s survive
        during bitter cold night.
      </p>

      <p>
        I was informed that since they do not use any fiber or cloth, they sleep on husk of wheat
        or rice. But I was still wondering, what they use to cover their body during cold nights.
        May be our Jain Digamber friends on net could provide us better information on the issue
        about life style of these supreme human beings.
      </p>

      <p>
        I call them supreme human being because; even Buddhist monks use at least a thin Chadder
        to cover themselves during bitter cold. Only difference could be that Buddhist monks stay
        for many months of a year in places where temperatures r much below minus degrees where as
        these Jain shadhu’s are in plains where maximum the temperature could go up to minus one or
        2 degrees.
      </p>

      <p>
        I remember seeing a Discovery Channel episode where American scientist did research on
        Buddhist monks, and were surprised to note that these monk could even dry a wet cloth
        rapped on their body during a cold night, using their body heat.
      </p>

      <p>
        Also, it was big surprise to note during our return journey via ‘Kasara’ station of
