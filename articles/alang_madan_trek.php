      <p>
        I had been trekking for last seven years now but still stuffs like rock climbing
        were practically new for me. This entire remained dream till my good friend Amit
        informed me that Mr Sanjay Lokhre had planned a trek at Alanggad, Madangad.
      </p>

      <p>
        These forts were kept away from our list of future treks, as little knowledge
        of climbing was necessary along with the instruments.
      </p>

      <p>
        So the trek was planned and it was decided to meet at Sanjay’s photo studio.
        At 12 am we all came together at the meeting point.
      </p>

      <p>
        We had our SUMO jeep with us for travelling. All the sacks were checked for
        necessary equipments and were packed on the top of the jeep.
      </p>

      <p>
        After packing Dilip prayed to god for our safety and we started our journey.
        As soon as the jeep turtled through the road front lights were put on which
        slashed the foggy night.
      </p>

      <p>
        At 1.30 in the night we reached Kasara ghat, had a cup of tea and resumed our
        journey. Early morning at 5 we reached Khed, mistakenly assuming it to be
        Ambewadi.
      </p>

      <p>
        After discovering our mistake we again got back in the jeep and finally at 6
        reached Ambewadi village.
      </p>

      <p>
        Having some refreshments we started our trek towards Alanggad at 7.15.
        As the sun rose, the light falling on the ranges made it look baroque.
      </p>

      <p>
        We felt minuscule in front of the prodigiousness of Mother Nature.
      </p>

      <p>
        Aiming at a “V” shaped valley near about 1200 meters above sea level we started
        our climb.
      </p>

      <p>
        With photographers exploiting every chance they got, the jungle felt like a
        divine studio set by gods themselves.
      </p>

      <p>
        Encountering a group rappelling on a 100-foot patch we chose that route instead
        of a narrow five-hour traverse.
      </p>

      <p>
        After reaching the top by 3.30 pm we saw a deep amphitheatre-like valley behind
        the mountain and a cave that possibly was a Buddhist vihar.
      </p>

      <p>
        Sanjay reached the cave last at 5 pm after removing all ropes. Evening tea,
        search for water and a candle-light dinner followed.
      </p>

      <p>
        The cooperation shown among us despite knowing each other for just 20 hours
        was heart-warming.
      </p>

      <p>
        At sunrise we explored the fort, water cisterns, ancient houses and the
        artificial lake.
      </p>

      <p>
        From the summit views of Kalsubai, Avandh, Patta, Bitangad, Harihar, Trimbak,
        Madangad, Kulanggad, Ratan, Ajoba and Harischandragad invited us again.
      </p>

      <p>
        After breakfast we marched towards Madangad, descending rock patches and
        traversing exposed ledges with utmost caution.
      </p>

      <p>
        Technical climbing again tested us and after ascending zig-zag steps we reached
        the Madan cave for the night.
      </p>

      <p>
        The sunset behind Kulung fort and the thrilling descent through the hole were
        unforgettable experiences.
      </p>

      <p>
        Despite a tense moment involving stuck ropes and strong winds, teamwork once
        again saved the day.
      </p>

      <p>
        Next morning the mist-filled Sahyadris unveiled a primeval spectacle of cloud
        seas and forested ridges.
      </p>

      <p>
        By 11.30 am we were back at Ambewadi and soon on our journey home.
      </p>

      <p class="font-semibold">
        “It is only when we leave some of our city bred ideas and wants behind and
        create space for something new and fresh that we are able to savour this
        beautiful gift of nature.”
      </p>

