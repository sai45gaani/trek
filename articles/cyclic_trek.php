    <article class="space-y-6 text-lg leading-relaxed text-gray-800 dark:text-gray-200">

      <p>
        As I sat in the cargo dabba of the Khopoli train (KP1) on the morning of Saturday, the 12th March,
        I had mixed feelings…. I was glad that I was ultimately going on the route I had planned a long
        time ago, but also uncertain of the route and whether we could complete it in the heat.
      </p>

      <p>
        Kaushal had joined me at Dombivli and we were headed towards a road between Pali and Raigad,
        which seemingly no one had heard of. It was there on the road map that I had, but then the map
        also showed several other roads, which I knew, didn't exist.
      </p>

      <p>
        Soon all the tentativeness was gone as we started cycling from Khopoli at 7.20 a.m. We wanted
        to make as much distance as we could before it became too hot. Taking the left turn just before
        Madh, we went past the Pali-35 milestone.
      </p>

      <p>
        Cycling in the fresh early morning was fun and we breezed along to take our first halt 17km
        from Pali, at 8.30, to take tea. The road was very good with few climbs. …mostly it was flat.
      </p>

      <p>
        I reached Pali at 9.30, Kaushal came along 15 minutes later. He had had cramps in his thighs
        and needed a break. Since we had had nothing solid since morning, we took a quick breakfast.
      </p>

      <p>
        I asked Kaushal if he would stay back at Pali or climb Sarasgad with me. He decided to give it
        a try. Sarasgad is a small, elegant fort, which looms large behind Pali.
      </p>

      <p>
        The route is very clear, can be called a ‘Rajmarga' and climbs up steeply and quickly. The
        staircase near the top is very smart work and a great piece of mountain architecture.
      </p>

      <p>
        Kaushal waited under a tree just after the staircase while I climbed to the top of the
        Balekilla after traversing around the cliffs. It had taken around 45 min to climb to the top
        from Pali.
      </p>

      <p>
        Taking a look around, I got good views of the peaks around, most of which I couldn't identify.
        We then hurried back down to reach Pali at the stroke of noon.
      </p>

      <p>
        We then had an excellent lunch (our photos just after the lunch show that we relished it).
        Making enquiries about the road to Raigad, via Boravali and Channata, we got negative response
        from several people near the temple.
      </p>

      <p>
        They didn’t know of any such road. Next we tried at the ST stand, but with same result. One
        conductor declared that the only way to reach Raigad was via Mahad.
      </p>

      <p>
        One other fellow knew of the trek route between Sarasgad and Raigad, but not the road. It was
        12.30 now and we had to decide quickly what was to be done.
      </p>

      <p>
        Kaushal was feeling tired and needed rest. Taking this into account, we decided to take a bus
        to Mahad. Changing the bus at Nagothane on the way, we reached Mahad at 4.15 p.m.
      </p>

      <p>
        Here Kaushal took a U-turn by deciding to go back home that same day. I probably didn’t
        realize the extent of his tiredness when we had started from Pali.
      </p>

      <p>
        I decided to continue alone to Raigad. The first 17 of the 24 km from Mahad to Raigad were
        perfectly flat and I covered them in just over an hour.
      </p>

      <p>
        It took another hour to do the last 7 km from Kolzhar to Pachad. That climb was very steep
        and my legs were already tired till then.
      </p>

      <p>
        Just when I felt I was stretching it too far I saw Pachad. Having snacks at Hotel Deshmukh,
        I started climbing up Raigad.
      </p>

      <p>
        According to my expectations I reached Raigad top at 8 p.m. The MTDC resort was full, but
        luckily a double room was available at the Ropeway guesthouse.
      </p>

      <p>
        A short walk in the beautiful starry night ended the day for me. I lay flat on the bed and
        fell far asleep.
      </p>

      <p>
        Early morning I had to make a choice – whether to see Raigad fort and then pedal down to
        Mahad, or go and explore 'the other road' to Raigad from Mangaon.
      </p>

      <p>
        I chose to explore that road. Speeding down to Pachad in 30mins, I had some quick snacks,
        packed some food and started pedaling at 7.
      </p>

      <p>
        The complex nature of the Sahyadris in this part fascinated me. In comparison to the 'main
        range and cliffs’ structure I had seen elsewhere, these mountains were totally different.
      </p>

      <p>
        I reached Pali breezing through the last 12 km in about 45 min. The lunch at Pali was most
        satisfying experience.
      </p>

      <p>
        I was lucky that my cycle didn’t cause any problem during the entire trip. I was even
        luckier when I got lift in a tempo from Pali to the Jogeshwari link-road junction.
      </p>

      <p>
        …where my sweet dirty room, and two buckets of hot water awaited me …..
      </p>

